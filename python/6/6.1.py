
# 定义一个列表 list



my_list = ["I","Love","Python"]
print(my_list)
print(type(my_list))


my_list = ["I",11,True]
print(my_list)
print(type(my_list))

#定义一个嵌套的列表
my_list = [[1,2,3],[4,5,6]]
print(my_list)
print(type(my_list))


# 列表索引  不要超过索引的位置 否则报错
my_list = ["I","Love","Python"]
print(my_list[0])
print(my_list[1])
print(my_list[2])
print(my_list[-3])
print(my_list[-2])
print(my_list[-1])
# 嵌套索引
my_list = [[1,2,3],[4,5,6]]
print(my_list[1][1])
print(my_list[-1][-1])
